package conditionStatements;

public class IfStatement {

	public static void main(String[] args) {
		int a = 10;

		if (a == 5) {
			System.out.println("a is equals to five");
		}
		if (a >= 5) {
			System.out.println("a is greater than or equals to five");
		}
		if (a <= 5) {
			System.out.println("a is less than or equals to five");
		}
		if (a == 10) {
			System.out.println("a is equals to ten");
		}

	}

}
